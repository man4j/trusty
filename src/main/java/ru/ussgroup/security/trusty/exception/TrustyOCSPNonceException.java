package ru.ussgroup.security.trusty.exception;

public class TrustyOCSPNonceException extends Exception {
	public TrustyOCSPNonceException() {
		super();
	}

	public TrustyOCSPNonceException(String message, Throwable cause) {
		super(message, cause);
	}

	public TrustyOCSPNonceException(String message) {
		super(message);
	}

	public TrustyOCSPNonceException(Throwable cause) {
		super(cause);
	}
}
